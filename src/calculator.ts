class Calculator {
    add(a: number, b: number): number {
        return a+b;
    }

    difference(a: number, b: number): number{
        return a-b;
    }

    multiplication(a: number, b: number): number{
        return a*b;
    }

    division(a: number, b: number) {

        return a/b;
    }
}

export {Calculator};